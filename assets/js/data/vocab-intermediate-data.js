  function getWordsByTheme(themeId) {
    const theme = getThemeById(themeId);
    return theme ? theme.words : [];
  }

  function getAllWords() {
    return THEMES.flatMap(t => t.words);
  }

  function getWordById(wordId) {
    return getAllWords().find(w => w.id === wordId) || null;
  }

  function getWordsByLevel(level) {
    return getAllWords().filter(w => w.level === level);
  }

  function getThemesByLevel(level) {
    return THEMES.filter(t => t.level === level);
  }

  // Get all words that have word family data
  function getWordFamilies() {
    return getAllWords().filter(w => w.family);
  }

  // Get unique root words (words where the word itself has siblings in family)
  function getFamilyGroups() {
    const words = getAllWords().filter(w => w.family);
    // Group by verb root or noun (main form)
    const groups = [];
    const seen = new Set();
    words.forEach(w => {
      if (!seen.has(w.id)) {
        seen.add(w.id);
        const f = w.family;
        const group = {
          root: w.word,
          translation: w.translation,
          word: w,
          verb: f.verb,
          noun: f.noun,
          adjective: f.adjective,
          adverb: f.adverb,
        };
        // Only include groups with 2+ parts
        const parts = [f.verb, f.noun, f.adjective, f.adverb].filter(Boolean);
        if (parts.length >= 2) groups.push(group);
      }
    });
    return groups;
  }

