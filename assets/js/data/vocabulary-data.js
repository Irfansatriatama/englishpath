  function getAllWords() {
    const all = [];
    THEMES.forEach(theme => {
      theme.words.forEach(w => {
        all.push({ ...w, themeId: theme.id, themeName: theme.nameID });
      });
    });
    return all;
  }

  function getThemes() {
    return THEMES;
  }

  function getThemeById(id) {
    return THEMES.find(t => t.id === id) || null;
  }

  function getWordsByTheme(themeId) {
    const theme = getThemeById(themeId);
    return theme ? theme.words : [];
  }

  function getWordById(id) {
    for (const theme of THEMES) {
      const found = theme.words.find(w => w.id === id);
      if (found) return { ...found, themeId: theme.id };
    }
    return null;
  }

  function getWordsByLevel(level) {
    const all = getAllWords();
    return all.filter(w => w.level === level);
  }

  function getTotalWords() {
    return getAllWords().length;
  }

